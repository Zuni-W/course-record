void uchr2uint(uint8_t* in, uint32_t* out)
{
	int i = 0;
	*out = 0;
	for (i = 0; i < 4; i++)
		*out = ((uint32_t)in[i] << (24 - i * 8)) ^ *out;
}

void uint2uchr(uint32_t in, uint8_t* out)
{
	int i = 0;
	for (i = 0; i < 4; i++)
		*(out + i) = (uint8_t)(in >> (24 - i * 8));
}

uint32_t left_move(uint32_t data, int length)
{
	uint32_t result = 0;
	result = (data << length) ^ (data >> (32 - length));
	return result;
}

