void round_fun(uint32_t* x, uint32_t k) {
	uint32_t A = x[1] ^ x[2] ^ x[3] ^ k;
	uint8_t A_chr[4];
	uint2uchr(A, A_chr);
	for (int i = 0; i < 4; i++) {
		uint8_t row = A_chr[i] >> 4;
		uint8_t col = A_chr[i] & 0b1111;
		A_chr[i] = S_Box[16 * row + col];
	}
	uint32_t B;
	uchr2uint(A_chr, &B);
	uint32_t temp = x[0] ^ B ^ left_move(B, 2) ^ left_move(B, 10) ^ left_move(B, 18) ^ left_move(B, 24);
	x[0] = x[1];
	x[1] = x[2];
	x[2] = x[3];
	x[3] = temp;
}

