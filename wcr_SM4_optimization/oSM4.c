uint32_t L(uint32_t B)
{
	uint32_t B2 = (B << 23) ^ (B >> 32 - 23);
	uint32_t B1 = (B << 13) ^ (B >> 32 - 13);
	uint32_t B0 = B ^ B1 ^ B2;
	return B0;
}//8283CB69
